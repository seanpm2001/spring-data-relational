CREATE TABLE SIMPLE_ENTITY
(
    ID   BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    NAME VARCHAR(100)
);

CREATE TABLE WITH_LIST
(
    ID   BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    NAME VARCHAR(100)
);

CREATE TABLE CHILD
(
    WITH_LIST   BIGINT NOT NULL REFERENCES WITH_LIST(ID),
    WITH_LIST_KEY INT NOT NULL,
    NAME VARCHAR(100)
);